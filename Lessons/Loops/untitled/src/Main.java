public class Main {
    public static void main(String[] args) {

        for(int i=0; i<=10; i++){
            System.out.println(i);
        }
        System.out.println("For dongusu bitti");

        int i = 0;
        while(i<10) {
            i++;
            System.out.println(i);
        }
        System.out.println("while dongusu bitti");
        int y = 1;
        do {
            System.out.println(y);
            y+=2;
        }while(y<10);
        System.out.println("Do-While dongusu bitti");


    }
}